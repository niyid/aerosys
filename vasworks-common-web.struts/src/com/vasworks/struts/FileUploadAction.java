package com.vasworks.struts;

import java.io.File;
import java.util.List;

/*
 * *
 * 
 * @author developer
 */
public interface FileUploadAction {

	/**
	 * Gets the uploads.
	 * 
	 * @return the uploads
	 */
	public abstract void setUploads(List<File> uploads);

	/**
	 * Sets the upload file names.
	 * 
	 * @param uploadFileNames the uploadFileNames to set
	 */
	public abstract void setUploadsFileName(List<String> uploadFileNames);

	/**
	 * Sets the upload content types.
	 * 
	 * @param uploadContentTypes the uploadContentTypes to set
	 */
	public abstract void setUploadsContentType(List<String> uploadContentTypes);

}