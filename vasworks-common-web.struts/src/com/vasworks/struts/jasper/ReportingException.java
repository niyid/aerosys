package com.vasworks.struts.jasper;

/**
 * @author developer
 * 
 */
public class ReportingException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7115470712213534809L;

	/**
	 * 
	 */
	public ReportingException() {
	}

	/**
	 * @param arg0
	 */
	public ReportingException(final String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public ReportingException(final Throwable arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public ReportingException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

}
